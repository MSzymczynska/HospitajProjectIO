    private void bSOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSOrderActionPerformed
        //public ProductOrder(String id, ProductQuantity productQuantity, Date date, String from, String to)
        if (tStorage.getSelectedRow() != -1) {
            ProductQuantity p = SMP.getStorage().get(tStorage.getSelectedRow());
            String id = "1";
            if (!SMP.getArchive().isEmpty()) {
                id = Integer.toString(Integer.parseInt(SMP.getArchive().get(SMP.getArchive().size() - 1).id) + 1);
            }

            ProductOrder po = new ProductOrder(id, new ProductQuantity(p.product, Integer.parseInt(txtSHowMuch.getText())), new Date(), "", "Sklep");
            SMP.order(po, "Sklep");
            ((STableModel) tStorage.getModel()).fireTableDataChanged();
        }
            txtSHowMuch.setText("");
    }//GEN-LAST:event_bSOrderActionPerformed

